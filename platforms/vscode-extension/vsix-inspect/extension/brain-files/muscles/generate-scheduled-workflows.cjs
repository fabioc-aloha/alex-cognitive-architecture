#!/usr/bin/env node
/**
 * @muscle generate-scheduled-workflows
 * @inheritance inheritable
 * @description Generate GitHub Actions workflow files from scheduled-tasks.json config
 * @version 1.0.0
 * @skill scheduled-tasks
 * @reviewed 2026-04-18
 * @platform windows,macos,linux
 * @requires node
 *
 * Reads .github/config/scheduled-tasks.json and generates one workflow YAML
 * per enabled task in .github/workflows/scheduled-*.yml
 *
 * Usage:
 *   node .github/muscles/generate-scheduled-workflows.cjs [--dry-run]
 * @currency 2026-04-20
 */

"use strict";

const fs = require("fs");
const path = require("path");

const DRY_RUN = process.argv.includes("--dry-run");

// -- Paths ------------------------------------------------------------------

const ROOT = findRepoRoot(process.cwd());
const CONFIG_PATH = path.join(ROOT, ".github", "config", "scheduled-tasks.json");
const WORKFLOWS_DIR = path.join(ROOT, ".github", "workflows");

function findRepoRoot(dir) {
  let current = dir;
  while (current !== path.dirname(current)) {
    if (fs.existsSync(path.join(current, ".github"))) return current;
    current = path.dirname(current);
  }
  return process.cwd();
}

// -- Templates --------------------------------------------------------------

/**
 * Generate a change-detection step for adaptive scheduling (AV2).
 * Uses git log to check if any watched paths changed since last successful run.
 * Falls back to running if the timestamp file doesn't exist (first run).
 */
function changeDetectionStep(task) {
  const cd = task.changeDetection;
  if (!cd || !cd.enabled) return '';
  const paths = (cd.paths || []).join(' ');
  const maxIdle = cd.maxIdleDays || 7;
  return `
      - name: Check for relevant changes (adaptive scheduling)
        id: changes
        run: |
          LAST_RUN_FILE=".github/config/.last-run-${task.id}"
          if [ ! -f "$LAST_RUN_FILE" ]; then
            echo "has_changes=true" >> $GITHUB_OUTPUT
            echo "First run — no timestamp file"
            exit 0
          fi
          LAST_RUN=$(cat "$LAST_RUN_FILE")
          DAYS_IDLE=$(( ($(date -u +%s) - $(date -u -d "$LAST_RUN" +%s)) / 86400 ))
          if [ "$DAYS_IDLE" -ge ${maxIdle} ]; then
            echo "has_changes=true" >> $GITHUB_OUTPUT
            echo "Force run: $DAYS_IDLE days idle (max: ${maxIdle})"
            exit 0
          fi
          CHANGED=$(git log --since="$LAST_RUN" --name-only --pretty=format: -- ${paths} | grep -c . || true)
          if [ "$CHANGED" -gt 0 ]; then
            echo "has_changes=true" >> $GITHUB_OUTPUT
            echo "$CHANGED file(s) changed since $LAST_RUN"
          else
            echo "has_changes=false" >> $GITHUB_OUTPUT
            echo "No changes since $LAST_RUN — skipping"
          fi
`;
}

/**
 * Generate a step to record last successful run timestamp.
 * Gated on changeDetection so it only records when the task actually ran.
 */
function recordRunStep(task) {
  const cd = task.changeDetection;
  if (!cd || !cd.enabled) return '';
  const gate = changeGate(task);
  return `
      - name: Record successful run timestamp${gate}
        run: |
          date -u +%Y-%m-%dT%H:%M:%SZ > ".github/config/.last-run-${task.id}"
          git add ".github/config/.last-run-${task.id}"
`;
}

/**
 * Returns the condition string for gating steps on change detection.
 */
function changeGate(task) {
  const cd = task.changeDetection;
  if (!cd || !cd.enabled) return '';
  return `
        if: steps.changes.outputs.has_changes == 'true'`;
}

function agentWorkflow(task) {
  const useWorktree = task.useWorktree === true;
  const cdStep = changeDetectionStep(task);
  const gate = changeGate(task);

  let branchStep = '';
  if (useWorktree) {
    branchStep = `
      - name: Create worktree branch
        id: branch${gate}
        run: |
          BRANCH="auto/${task.id}-$(date -u +%Y%m%d-%H%M%S)"
          git checkout -b "$BRANCH"
          echo "branch=$BRANCH" >> $GITHUB_OUTPUT
`;
  }

  return `# Auto-generated by generate-scheduled-workflows.cjs — do not edit manually
name: "Scheduled: ${task.name}"

on:
  schedule:
    - cron: "${task.schedule}"
  workflow_dispatch: {}

permissions:
  contents: write
  issues: write
  pull-requests: write

jobs:
  create-copilot-task:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v5
        with:
          fetch-depth: 0
${cdStep}
      - name: Check for duplicate open issues
        id: check${gate}
        env:
          GH_TOKEN: \${{ github.token }}
        run: |
          OPEN=$(gh issue list --label automated,${task.id} --state open --json number --jq 'length')
          echo "open=$OPEN" >> $GITHUB_OUTPUT
${branchStep}
      - name: Create issue for Copilot
        id: create
        if: steps.check.outputs.open == '0'${gate ? " && " + gate.trim().replace("if: ", "") : ""}
        env:
          GH_TOKEN: \${{ github.token }}
        run: |
          ISSUE_URL=$(gh issue create \\
            --title "${task.name}: $(date -u +%Y-%m-%d-%H%M)" \\
            --body-file "${task.promptFile}" \\
            --label automated,${task.id})
          echo "issue_url=$ISSUE_URL" >> $GITHUB_OUTPUT

      - name: Assign Copilot to issue
        if: steps.create.outputs.issue_url
        continue-on-error: true
        env:
          GH_TOKEN: \${{ github.token }}
        run: |
          gh issue edit "\${{ steps.create.outputs.issue_url }}" --add-assignee copilot || echo "Could not assign Copilot — assign manually"
`;
}

/**
 * Determine the runner command and setup step based on muscle file extension.
 */
function muscleRunner(muscle) {
  if (muscle.endsWith('.ps1')) return { cmd: 'pwsh', setup: '' };
  if (muscle.endsWith('.py')) return { cmd: 'python', setup: '' };
  return { cmd: 'node', setup: `
      - uses: actions/setup-node@v5{gate}
        with:
          node-version: 22
` };
}

function directWorkflow(task) {
  const args = task.muscleArgs ? ` ${task.muscleArgs.join(" ")}` : "";
  const cdStep = changeDetectionStep(task);
  const gate = changeGate(task);
  const recordStep = recordRunStep(task);
  const runner = muscleRunner(task.muscle);
  const setupStep = runner.cmd === 'node'
    ? `
      - uses: actions/setup-node@v5${gate}
        with:
          node-version: 22
`
    : '';

  return `# Auto-generated by generate-scheduled-workflows.cjs — do not edit manually
name: "Scheduled: ${task.name}"

on:
  schedule:
    - cron: "${task.schedule}"
  workflow_dispatch: {}

permissions:
  contents: write
  pull-requests: write

jobs:
  run-task:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v5
        with:
          fetch-depth: 0
${cdStep}${setupStep}
      - name: Run ${task.id}${gate}
        run: ${runner.cmd} ${task.muscle}${args}
${recordStep}
      - name: Create PR if changes exist${gate}
        env:
          GH_TOKEN: \${{ github.token }}
        run: |
          git diff --quiet && exit 0
          BRANCH="auto/${task.id}-$(date -u +%s)"
          git checkout -b "$BRANCH"
          git config user.name "github-actions[bot]"
          git config user.email "github-actions[bot]@users.noreply.github.com"
          git add -A
          git commit -m "chore(scheduled): ${task.id} $(date -u +%Y-%m-%d)"
          git push origin "$BRANCH"
          cat > /tmp/pr-body.md <<'EOF'
## Automated Task: ${task.name}

${task.description}

---

*Generated by \`scheduled-${task.id}.yml\` · [Edit config](../.github/config/scheduled-tasks.json)*
EOF
          gh pr create \\
            --title "Scheduled: ${task.name} $(date -u +%Y-%m-%d)" \\
            --body-file /tmp/pr-body.md \\
            --label automated \\
            --base main
`;
}

// -- Main -------------------------------------------------------------------

function main() {
  if (!fs.existsSync(CONFIG_PATH)) {
    console.error(`Config not found: ${CONFIG_PATH}`);
    process.exit(1);
  }

  const config = JSON.parse(fs.readFileSync(CONFIG_PATH, "utf-8"));
  const tasks = config.tasks || [];
  const enabledTasks = tasks.filter((t) => t.enabled);

  console.log(`Found ${tasks.length} tasks, ${enabledTasks.length} enabled`);

  if (!fs.existsSync(WORKFLOWS_DIR)) {
    fs.mkdirSync(WORKFLOWS_DIR, { recursive: true });
  }

  // Clean up old scheduled workflows that are no longer in config
  const existingWorkflows = fs.readdirSync(WORKFLOWS_DIR)
    .filter((f) => f.startsWith("scheduled-") && f.endsWith(".yml"));

  const expectedFiles = new Set(enabledTasks.map((t) => `scheduled-${t.id}.yml`));

  for (const file of existingWorkflows) {
    if (!expectedFiles.has(file)) {
      const fullPath = path.join(WORKFLOWS_DIR, file);
      if (DRY_RUN) {
        console.log(`[dry-run] Would remove: ${file}`);
      } else {
        fs.unlinkSync(fullPath);
        console.log(`Removed stale workflow: ${file}`);
      }
    }
  }

  // Generate workflows for enabled tasks
  let generated = 0;
  for (const task of enabledTasks) {
    const filename = `scheduled-${task.id}.yml`;
    const filepath = path.join(WORKFLOWS_DIR, filename);

    let content;
    if (task.mode === "agent") {
      if (!task.promptFile) {
        console.warn(`Skipping ${task.id}: mode=agent but no promptFile`);
        continue;
      }
      content = agentWorkflow(task);
    } else if (task.mode === "direct") {
      if (!task.muscle) {
        console.warn(`Skipping ${task.id}: mode=direct but no muscle`);
        continue;
      }
      content = directWorkflow(task);
    } else {
      console.warn(`Skipping ${task.id}: unknown mode "${task.mode}"`);
      continue;
    }

    if (DRY_RUN) {
      console.log(`\n[dry-run] Would write: ${filename}`);
      console.log(content);
    } else {
      fs.writeFileSync(filepath, content, "utf-8");
      console.log(`Generated: ${filename}`);
    }
    generated++;
  }

  console.log(`\nDone. ${generated} workflow(s) ${DRY_RUN ? "would be " : ""}generated.`);
}

try {
  main();
} catch (err) {
  console.error("generate-scheduled-workflows failed:", err.message);
  process.exit(1);
}
